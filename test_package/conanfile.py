import os

from conans import ConanFile, CMake, tools


class UdevxxTestConan(ConanFile):
    settings = None
    generators = "cmake"

    def build(self):
        cmake = CMake(self)
        cmake.configure()
        cmake.build()

    def test(self):
        if not tools.cross_building(self.settings):
            os.chdir("bin")
            self.run(".%sexample" % os.sep)
